public class Symbol
{
    private String symbolImage = "";
    private String symbolName = "";

    public void setSymbolName(String name)
    {
        symbolName = name;
    }

    public String getSymbolName()
    {
        return symbolName;
    }

    public void setSymbolImage(String image)
    {
        symbolImage = image;
    }

    public String getSymbolImage()
    {
        return symbolImage;
    }

}
